#include "vm.h"

t_vm		*init_vm(void)
{
	int 	n;
	t_vm	*vm;

	n = -1;
	vm = (t_vm *)malloc(sizeof(t_vm));
	while (++n < 4)
		vm->players[n] = NULL;
	return(vm);
}